import UIKit

extension UIFont {
    
    // Regular
    static func ralewayRegular14() -> UIFont? {
        return UIFont(name: "Raleway-Regular", size: 14)
    }
    
    static func ralewayRegular16() -> UIFont? {
        return UIFont(name: "Raleway-Regular", size: 16)
    }
    
    static func ralewayRegular20() -> UIFont? {
        return UIFont(name: "Raleway-Regular", size: 20)
    }
    
    static func ralewayRegular24() -> UIFont? {
        return UIFont(name: "Raleway-Regular", size: 24)
    }
    
    
    // SemiBold
    static func ralewaySemiBold14() -> UIFont? {
        return UIFont(name: "Raleway-SemiBold", size: 14)
    }
    
    static func ralewaySemiBold16() -> UIFont? {
        return UIFont(name: "Raleway-SemiBold", size: 16)
    }
}
